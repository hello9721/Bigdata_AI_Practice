# 통계적 의사결저의 과정 - 가설검정


# 가설검정의 절차

# 1. 귀무가설과 대립가설의 설정
#     > 귀무가설 - 분석한 독립변수와 종속변수는 서로 관련성이 없음.
#     > 대립가설 - 분석한 독립변수와 종속변수는 서로 관련성이 있음.

# 2. 유의수준 설정
#     > 유의수준을 기준으로 임계값을 계산

# 3. 표본 수집 및 가설검정 진행
#     > 검정통계량과 유의확률(p) 계산
#     > 검정통계량 - 귀무가설 기각 여부를 판단하고자 하는 값
#     > 유의확률 - 귀무가설 하에서 검정통계량 보다 더 극단적인 값이 나올 확률

# 4. 가설검정
#     > 유의확률이나 검정통계량 값을 활용해 검정 진행