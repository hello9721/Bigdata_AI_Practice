# 통게분석 수행

# 빈도분석 : 변수의 분포 제공 <- 모든 척도
# 교차분석(카이제곱) : 분포와 백분율을 나타내주는 교차표를 작성하여 독립성과 관련성 분석 <- 명목 / 서열
# 요인분석 : 상관관계가 높은 것끼리 묶어서 변수를 단순화 <- 등간 / 비율
# 신뢰도분석 : 요인 분석으로 추출된 요일들이 동질적인 변수들로 구성되어 있는가 파악 <- 등간 / 비율
# 상관관계분석 : 가설 검정 이전에 모든 연구가설에 사용되는 측정변수 간의 관계 정도를 제시하여 변수 간의 관련성에 윤곽제시
#                <- 피어슨 - 등간 / 비율 | 스피어만 - 서열
# 회귀분석 : 독립변수가 종속변수에 어떠한 영향을 미치는지 파악 <- 등간 / 비율
# t-검정 : 종속변수에 대한 독립 변수의 집단 간 평균차이 검정
#                <- 독립변수 - 명목 | 종속변수 - 등간 / 비율
# 분산분석(ANOVA) : 3개의 다른 집단 간 평균의 차이를 구함
#                <- 독립변수 - 명목 | 종속변수 - 등간 / 비율


